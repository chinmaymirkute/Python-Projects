num_1 = float(input("Enter first number :-"))
num_2 = float(input("Enter second number :-"))


temp = num_1
num_1 = num_2
num_2 = temp

print('The value of first number after swapping: {}'.format(num_1))
print('The value of second number after swapping: {}'.format(num_2))
